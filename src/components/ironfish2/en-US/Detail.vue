    <p>Privacy-focused blockchains before 2023 all have drawbacks in terms of privacy, accessibility, or both. <strong>Iron Fish does not compromise on privacy</strong>, while still being extremely easy to use. The application layer is written in Typescript, a popular programming language, which allows for simple end-user integrations and interactions.</p>

    <p>The key innovation of Iron Fish is the way it provides full privacy <strong>while still being compliant with government regulations</strong>. Zcash and Monero are 100% private, meaning criminals can conduct money laundering and other illicit transactions on those chains.</p>

